# 줄바꿈 명령 \n , +=, str, int, round 이해하기
str1 = "abc \"abc\" abc";
print(str1);

str2 = "abc\nabc";
print(str2);

result = 10 * 10 * 10;
print(result);

num1 = 10;
num1 += 1; #num1 = num1 + 1; (전 값보다 속도가더느림)
print(num1);

a = '10';
b = 10;
print(a + str(b));

e1 = 13.63232;
print(int(e1));
print(round(e1));








